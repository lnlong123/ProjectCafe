    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        // TODO add your handling code here:
        String sql = "";
        boolean check;
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn hình thức thống kê");
            check = false;
        } else if (jRadioButton1.isSelected()) {
            String dateStart = sdf.format(JDateStartChoose.getDate());
            String dateEnd = sdf.format(JdateEndChoose.getDate());
            if (dateStart.compareTo(dateEnd) > 0) {
                JOptionPane.showMessageDialog(rootPane, "Ngày bắt đầu phải nhỏ hơn ngày kết thúc");
                check = false;
            } else {
                sql = "SELECT * FROM orderr JOIN employee ON orderr.EmpID = employee.Username LEFT JOIN tables ON orderr.TableID = tables.TableID "
                        + "LEFT JOIN customer ON orderr.CusId = customer.IDCus Where orderr.Status = 1 AND orderr.DateOrder >= '" + dateStart + "' AND orderr.DateOrder <= '" + dateEnd + "'";
                check = true;
            }
        } else if (jRadioButton2.isSelected()) {
            String idEmp = listUser.get(jComboBox1.getSelectedIndex()).getUserName();
            sql = "SELECT * FROM orderr JOIN employee ON orderr.EmpID = employee.Username LEFT JOIN tables ON orderr.TableID = tables.TableID "
                    + "LEFT JOIN customer ON orderr.CusId = customer.IDCus Where orderr.Status = 1 AND orderr.EmpID = '" + idEmp + "'";
            check = true;
        } else {
            check = false;
        }
        if (check) {
            try {
                Statement statement = db.connect().createStatement();
                ResultSet result = statement.executeQuery(sql);
                ShowBill(result);
            } catch (SQLException ex) {

            }
        }
    }//GEN-LAST:event_btnActionPerformed

