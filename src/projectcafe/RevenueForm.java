    public void ShowBill(ResultSet result) {
        DefaultTableModel model = (DefaultTableModel) OrderTable.getModel();
        int SumMoney = 0, SumSale = 0;
        try {
            model.setRowCount(0);
            while (result.next()) {
                String rows[] = new String[7];
                rows[0] = result.getString("IDOrder");
                rows[1] = result.getString("DateOrder");
                rows[2] = result.getString("NameEmp");
                if (result.getString("Name") == null) {
                    rows[3] = "Khách mang đi";
                } else {
                    rows[3] = result.getString("Name");
                }
                if (result.getString("CusName") == null) {
                    rows[4] = "Khách vãng lai";
                } else {
                    rows[4] = result.getString("CusName");
                }
                rows[5] = result.getString("Total");
                rows[6] = result.getString("Points");
                SumMoney += result.getInt("Total");
                SumSale += result.getInt("Points");
                model.addRow(rows);
            }
            lbSum.setText(String.valueOf(SumMoney) + " " + "VNĐ");
            lbSale.setText(String.valueOf(SumSale) + " " + "VNĐ");
            lbcount.setText(String.valueOf(OrderTable.getRowCount()) + " " + "hoá đơn");
            lbMoney.setText(String.valueOf(SumMoney - SumSale) + " " + "VNĐ");
        } catch (Exception e) {
        }
    }

