    private void ShowCustomer(ResultSet rs) {
        try {
            DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                String rows[] = new String[6];
                rows[0] = rs.getString("IDCus");
                rows[1] = rs.getString("CusName");
                rows[2] = rs.getString("DateAdd");
                rows[3] = rs.getString("Phone");
                rows[4] = rs.getString("Email");
                rows[5] = rs.getString("points");
                model.addRow(rows);
            }
        } catch (SQLException ex) {
            System.err.println(ex);
        }
    }

