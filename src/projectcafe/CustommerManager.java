    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO customer(IDCus, CusName, DateAdd, Phone, Email) VALUES (?,?,?,?,?)";
        if (checkText()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (!db.checkid(tfIdCus.getText(), db.getCustommer())) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng đã tồn tại");
        } else {
            try {
                PreparedStatement prest = db.connect().prepareStatement(sql);
                prest.setString(1, tfIdCus.getText());
                prest.setString(2, TfNameCus.getText());
                java.sql.Date date = new java.sql.Date(new java.util.Date().getTime());
                prest.setDate(3, date);
                prest.setString(4, tfPhone.getText());
                prest.setString(5, tfEmail.getText());
                if (prest.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Thêm thành viên thành công.");
                    reset();
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm thành viên thất bại.");
                }
            } catch (Exception e) {
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

