    private void TfNameCusKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TfNameCusKeyPressed
        // TODO add your handling code here
        char a = evt.getKeyChar();
        if (Character.isLetter(a) || Character.isISOControl(a) || Character.isSpaceChar(a) || evt.getKeyCode() >=16 && evt.getKeyCode()<=20) {
            TfNameCus.setEditable(true);
            lbNoti.setText("");
        } else {
            TfNameCus.setEditable(false);
            lbNoti.setText("Vui lòng nhập vào kí tự không phải là số hoặc kí tự đặc biệt");
        }
    }//GEN-LAST:event_TfNameCusKeyPressed

