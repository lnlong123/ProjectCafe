    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn mở bàn ", "WARNING", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            String sql = "INSERT INTO orderr(TableID) VALUES (?)";
            try {
                PreparedStatement prest = db.connect().prepareStatement(sql);
                prest.setString(1, btnOpen.getName());
                if (prest.executeUpdate() > 0) {
                    db.UpdateSttTable("Có người", btnOpen.getName());
                    JPBan.removeAll();
                    ShowBan();
                    JPBan.updateUI();
                    JOptionPane.showMessageDialog(null, "Mở bàn thành công");
                    UpdateStatus(btnOpen.getName());
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnOpenActionPerformed

    private void btnHuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHuyActionPerformed
        try {
            // TODO add your handling code here:
            ArrayList<Product> listPro = new ArrayList<>();
            ResultSet rs = db.getOrderDetails(Integer.parseInt(lbOrderID.getText()));
            while (rs.next()) {
                Product pro = new Product(rs.getString("IdProduct"), rs.getString("ProductName"), rs.getInt("Price"));
                listPro.add(pro);
            }

            int i = tblOrder.getSelectedRow();
            int res = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn huỷ món", "WARNING", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {
                String sql = "Delete from orderdetails where IdOrder = ? AND IdProduct = ?";
                PreparedStatement st;
                st = conn.prepareStatement(sql);
                st.setInt(1, Integer.parseInt(lbOrderID.getText()));
                st.setString(2, (listPro.get(i).getID()));
                if (st.executeUpdate() > 0) {
                    btnHuy.setEnabled(false);
                    JOptionPane.showMessageDialog(this, "Huỷ món thành công");
                    ShowOrderTotable(tblOrder, db.getOrderDetails(Integer.valueOf(lbOrderID.getText())));
                    lbTongTien.setText(String.valueOf(db.getTotalMoney(db.getOrderDetails(Integer.parseInt(lbOrderID.getText()))))+" VND");
                } else {
                    JOptionPane.showMessageDialog(null, "Huỷ món thất bại");
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Đã xảy ra lỗi");
        }
    }//GEN-LAST:event_btnHuyActionPerformed

