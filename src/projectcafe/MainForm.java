    public void getOrder(Integer TableID) {
        ResultSet result = null;
        String sql = "SELECT IDOrder FROM orderr where TableID =" + TableID + " AND Status = 0 ";
        try {
            Statement statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(sql);
            while (rs.next()) {
                lbOrderID.setText(rs.getString("IDOrder"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Đã xảy ra lỗi");
        }
    }

    public static void ShowOrderTotable(JTable tbl, ResultSet rs) {
        try {
            DefaultTableModel model = (DefaultTableModel) tbl.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                String rows[] = new String[3];
                rows[0] = rs.getString("ProductName");
                rows[1] = rs.getString("Amount");
                rows[2] = rs.getString("Price");
                model.addRow(rows);
            }
        } catch (SQLException ex) {

        }
    }

    public void UpdateStatus(String TableId) {
        getOrder(Integer.parseInt(TableId));
        ShowOrderTotable(tblOrder, db.getOrderDetails(Integer.parseInt(lbOrderID.getText())));
        btnOpen.setVisible(false);
        lbStatus.setText("Có người");
        pnID.setVisible(true);
        btnHuy.setEnabled(false);
        lbStatus.setVisible(true);
        lbTongTien.setText(String.valueOf(db.getTotalMoney(db.getOrderDetails(Integer.parseInt(lbOrderID.getText()))))+" VND");  
    }

    public void ShowBan() {
        try {
            ResultSet rs = db.getTable();
            while (rs.next()) {
                JButton btn = new JButton();
                btn.setText(rs.getString("Name"));
                btn.setName(rs.getString("TableID"));
                btn.setPreferredSize(new Dimension(103, 60));
                btn.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/Household-Table-icon.png")));
                if (rs.getString("Status").equals("Trống")) {
                    btn.setBackground(Color.WHITE);
                } else {
                    btn.setBackground(Color.RED);
                }
                JPBan.add(btn);
                btn.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        PanelOder.setVisible(true);
                        LbTenBan.setText(btn.getText());
                        btnOpen.setName(btn.getName());
                        if (btn.getBackground().equals(Color.RED)) {
                            UpdateStatus(btn.getName());
                        } else {
                            lbStatus.setText("Trống");
                            btnOpen.setVisible(true);
                            pnID.setVisible(false);
                        }
                    }
                });
            }
        } catch (SQLException ex) {

        }
    }

