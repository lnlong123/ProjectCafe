    private void showComboBox() {
        ArrayList<ProductType> ListProductType = db.ListProductType();
        cbbLoaiSP.removeAllItems();
        for (ProductType pro : ListProductType) {
            cbbLoaiSP.addItem(pro.getTypeName());
        }
    }

    private void ShowData(ArrayList<ProductType> ListProductType) {
        DefaultTableModel tableProductType = (DefaultTableModel) tblLoaiSp.getModel();
        try {
            tableProductType.setRowCount(0);
            for (ProductType pro : ListProductType) {
                String rows[] = new String[2];
                rows[0] = pro.getIDType(); // lấy dữ liệu tại cột số 1 (ứng với mã hàng)
                rows[1] = pro.getTypeName(); // lấy dữ liệu tai cột số 2 ứng với tên hàng
                tableProductType.addRow(rows); // đưa dòng dữ liệu vào tableModel để hiện thị lên jtable
            }
        } catch (Exception e) {
        }
        btnThem.setEnabled(true);
        btnXoa.setEnabled(false);
        BtnSua.setEnabled(false);
    }

    public void ShowDataProduct(ArrayList<Product> ListProduct) {
        DefaultTableModel tableProduct = (DefaultTableModel) tblProduct.getModel();
        tableProduct.setRowCount(0);
        for (Product pro : ListProduct) {
            String rows[] = new String[4];
            rows[0] = pro.getID();
            rows[1] = pro.getProductName();
            rows[2] = pro.getTypeName();
            String price = Integer.toString(pro.getPrice());
            rows[3] = price;
            tableProduct.addRow(rows); // đưa dòng dữ liệu vào tableModel để hiện thị lên jtable
        }
    }

