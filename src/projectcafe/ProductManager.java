    private void btnThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSPActionPerformed
        // TODO add your handling code here:
        if (tfProductName.getText().equals("") || tfProductPrice.getText().equals("") || TfIdProduct.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (!CheckidProduct()) {
            JOptionPane.showMessageDialog(null, "Mã sản phẩm đã tồn tại");
        } else {
            try {
                String sql = "INSERT INTO product VALUES (?,?,?,?)";
                PreparedStatement prest = conn.prepareStatement(sql);
                prest.setString(1, TfIdProduct.getText());
                prest.setString(2, tfProductName.getText());
                ArrayList<ProductType> st = db.ListProductType();

                ProductType productType = st.get(cbbLoaiSP.getSelectedIndex());
                String id = productType.getIDType();
                prest.setString(3, id);
                prest.setInt(4, Integer.parseInt(tfProductPrice.getText()));
                if (prest.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Thêm sản phẩm thành công.");
                    resetText();
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm sản phẩm thất bại.");
                }
            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_btnThemSPActionPerformed

    private void BtnSuaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSuaSPActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE product SET IdProduct = ?,ProductName = ?, IdType = ?, Price = ? WHERE IdProduct = ?";
        int i = tblProduct.getSelectedRow();
        if (TfIdProduct.getText().equals("") || tfProductName.getText().equals("") || tfProductPrice.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
        if (CheckidProduct() || TfIdProduct.getText().equals(tblProduct.getValueAt(i, 0))) {
            try {
                PreparedStatement pres = conn.prepareStatement(sql);
                pres.setString(1, TfIdProduct.getText());
                pres.setString(2, tfProductName.getText());
                ArrayList<ProductType> pro = db.ListProductType();
                ProductType productType = pro.get(cbbLoaiSP.getSelectedIndex());
                String id = productType.getIDType();
                pres.setString(3, id);
                try {
                    pres.setInt(4, Integer.parseInt(tfProductPrice.getText()));
                } catch (NumberFormatException w) {
                    JOptionPane.showMessageDialog(null, "Giá phải là số");
                }
                pres.setString(5, (String) tblProduct.getValueAt(i, 0));
                if (pres.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    resetText();
                }
            } catch (SQLException ex) {
                System.err.println(ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm đã tồn tại", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnSuaSPActionPerformed

