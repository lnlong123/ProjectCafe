    public void ShowData(ArrayList<User> listEmp) {
        DefaultTableModel tableModel = (DefaultTableModel) tblNhanvien.getModel();
        tableModel.setRowCount(0);
        for (User u : listEmp) {
            String rows[] = new String[9];
            rows[0] = u.getUserName();
            rows[1] = u.getName();
            rows[2] = u.getIdCart();
            rows[3] = u.getGender();
            rows[4] = u.getBirthday();
            rows[5] = u.getPhone();
            rows[6] = u.getEmail();
            rows[7] = u.getAddress();
            rows[8] = u.getPasswd();
            tableModel.addRow(rows); // đưa dòng dữ liệu vào tableModel để hiện thị lên jtable
        }
    }

    public ResultSet search(String txt) {
        ResultSet result = null;
        String sql = "SELECT * FROM `employee` WHERE NameEmp LIKE '%" + tfSearch.getText() + "%' "
                + "OR Username LIKE '%" + tfSearch.getText() + "%' ";
        try {
            Statement statement = db.connect().createStatement();
            return statement.executeQuery(sql);
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return result;
    }

