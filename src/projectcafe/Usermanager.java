    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String sql = " UPDATE employee SET Username = ?,NameEmp = ?,IdCart = ?,Gender = ?, Birthday = ?, Phone = ?, Email = ?, Address=?,passwd =? WHERE Username = ?";

        String date = sdf.format(jDateChooser1.getDate());
        int i = tblNhanvien.getSelectedRow();
        if (checkRong()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (tfUserName.getText().equals(tblNhanvien.getValueAt(i, 0)) || db.checkid(tfUserName.getText(), db.getEmployee())) {
            try {
                PreparedStatement pres = db.connect().prepareStatement(sql);
                pres.setString(1, tfUserName.getText());
                pres.setString(2, tfName.getText());
                pres.setString(3, tfIdCart.getText());
                pres.setString(4, Cbbgt.getSelectedItem().toString());
                pres.setString(5, date);
                pres.setString(6, tfSdt.getText());
                pres.setString(7, tfEmail.getText());
                pres.setString(8, tfDiachi.getText());
                pres.setString(9, tfPasswd.getText());
                pres.setString(10, (String) tblNhanvien.getValueAt(i, 0));
                if (pres.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    resetText();
                }
            } catch (SQLException ex) {
                System.err.println(ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tên tài khoản đã tồn tại", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String sql = "INSERT INTO employee(Username, NameEmp, Gender, Birthday, Phone, Email, Address, passwd) VALUES (?,?,?,?,?,?,?,?)";
        String date = sdf.format(jDateChooser1.getDate());
        if (checkRong()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (!db.checkid(tfUserName.getText(), db.getEmployee())) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
        } else {
            try {
                PreparedStatement prest = db.connect().prepareStatement(sql);
                prest.setString(1, tfUserName.getText());
                prest.setString(2, tfName.getText());
                prest.setString(3, Cbbgt.getSelectedItem().toString());
                prest.setString(4, date);
                prest.setString(5, tfSdt.getText());
                prest.setString(6, tfEmail.getText());
                prest.setString(7, tfDiachi.getText());
                prest.setString(8, tfPasswd.getText());
                if (prest.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Thêm thành viên thành công.");
                    resetText();

                } else {
                    JOptionPane.showMessageDialog(null, "Thêm thành viên thất bại.");
                }

            } catch (Exception e) {
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

