    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int i = tblBan.getSelectedRow();
        int res = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xoá loại sản phẩm", "Nhắc", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM tables WHERE TableID = ? ";
            PreparedStatement st;
            try {
                st = db.connect().prepareStatement(sql);
                st.setString(1, (String) tblBan.getValueAt(i, 0));
                if (st.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "Xoá thành công");
                    reset();
                } else {
                    JOptionPane.showMessageDialog(null, "Xoá không thành công");
                }
            } catch (SQLException ex) {
                System.err.println(ex);

            }
        }

    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE tables SET TableID = ?,Name = ? WHERE TableID = ?";
        int i = tblBan.getSelectedRow();
        if (tfID.getText().equals("") || tfName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (tfID.getText().matches(".*\\D")) {
            JOptionPane.showMessageDialog(this, "Mã phải là số", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (tfID.getText().equals(tblBan.getValueAt(i, 0)) || checkid()){
            try {
                PreparedStatement pres = db.connect().prepareStatement(sql);
                pres.setString(1, tfID.getText());
                pres.setString(2, tfName.getText());
                pres.setString(3, (String) tblBan.getValueAt(i, 0));
                if (pres.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Sửa thành công");
                    reset();
                }
            } catch (SQLException ex) {
                System.err.println(ex);
            }
        } else{
            JOptionPane.showMessageDialog(this, "Mã bàn đã tồn tại", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_btnCapNhatActionPerformed

