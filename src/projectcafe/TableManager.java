    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        checkid();
        String sql = "INSERT INTO tables(TableID,Name) VALUES (?,?)";
        if (tfID.getText().equals("") || tfName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (tfID.getText().matches(".*\\D")) {
            JOptionPane.showMessageDialog(this, "Mã phải là số", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else if (checkid() == false) {
            JOptionPane.showMessageDialog(this, "Mã bàn đã tồn tại", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                PreparedStatement prest = db.connect().prepareStatement(sql);
                prest.setString(1, tfID.getText());
                prest.setString(2, tfName.getText());
                if (prest.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Thêm bàn thành công.");
                    reset();
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm thành viên thất bại.");
                }
            } catch (Exception e) {
                System.err.println(e);
            }
        }

    }//GEN-LAST:event_btnThemActionPerformed

