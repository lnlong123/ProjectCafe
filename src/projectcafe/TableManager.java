    private ResultSet searchTable(String txt) {
        ResultSet result = null;
        String sql = "SELECT * FROM `tables` WHERE TableID LIKE '%" + tfSearch + "%' "
                + "OR Name LIKE '%" + tfSearch.getText() + "%' ";
        try {
            Statement statement = db.connect().createStatement();
            result = statement.executeQuery(sql);
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return result;
    }

    private void ViewBan(ResultSet result) {
        DefaultTableModel tbl = (DefaultTableModel) tblBan.getModel();
        try {
            tbl.setRowCount(0);
            while (result.next()) {
                String rows[] = new String[2];
                rows[0] = result.getString("TableID");
                rows[1] = result.getString("Name");
                tbl.addRow(rows);
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    }

    private void reset() {
        tfID.setText("");
        tfName.setText("");
        btnThem.setEnabled(true);
        btnCapNhat.setEnabled(false);
        btnXoa.setEnabled(false);
        tfSearch.setText("");
        ViewBan(db.getTable());
    }

    private boolean checkid() {
        boolean check = true;
        for (int i = 0; i < tblBan.getRowCount(); i++) {
            if (tfID.getText().equals(tblBan.getValueAt(i, 0))) {
                check = false;
                break;
            } else {
                check = true;
            }
        }
        return check;
    }

